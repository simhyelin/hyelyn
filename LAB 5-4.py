prime_list = [2, 3, 5, 7]
print('1에서 10까지의 소수 :', prime_list)
print('최솟값 :', min(prime_list))
print('최댓값 :', max(prime_list))
print('합계 :', sum(prime_list))
print('평균 :', sum(prime_list) / len(prime_list))

